package GenericStrings;

public class CharToInt 
{
	 
	public static void main(String[] args) 
	{
		char ch='A';
		int a=(int)ch;
		System.out.println(a);
		
		int b=97;
		char B=(char)b;
		System.out.println(B);
		
		char s='C';
		String str=Character.toString(s);
		System.out.println(str);
		
		// 0-9 (48-57)
		// A-Z (65-90)
		// a-z (97-122)
		
	}
	
	
	

}
